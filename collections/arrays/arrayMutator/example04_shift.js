/**
 * Usando o shift
 * 
 * O shift remove o elemento no início do array
 * 
 */
const languages = ["Ruby", "Go", "C", "Python", "Java"]
console.log("Array inicial => ", languages)

console.log("Removendo elemento do início da fila => ", languages.shift())
console.log("Removendo elemento do início da fila => ", languages.shift())
console.log("Imprimindo array após shift() => ", languages)