/**
 * Usando o pop
 * 
 * O pop remove o elemento no final do array
 * 
 * Ao remover o elemento o length já reflete a nova quantidade
 */
const languages = ["C", "Python", "Java", "Ruby", "Go"]
console.log("Array inicial => ", languages)
console.log("Length do array inicial => ", languages.length)

console.log("Imprindo elemento removido do fim da fila => ", languages.pop())
console.log("Imprindo elemento removido do fim da fila => ", languages.pop())

console.log("Novo array de languages após remover elementos => ", languages)
console.log("Length do array após remover elementos => ", languages.length)



